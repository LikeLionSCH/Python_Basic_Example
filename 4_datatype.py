'''반복 불가능한 자료형
int, float, complex
'''

#--------------------------- 정수형 ---------------------------#

a = 1
b = 2
c = 3_000_000

print("a :", a, ", type(a) :", type(a)) # a : 1 , type(a) : <class 'int'>
print("b :", b, ", type(b) :", type(b)) # a : 2 , type(b) : <class 'int'>
print("c :", c, ", type(c) :", type(c)) # a : 3000000 , type(c) : <class 'int'>

print()

#--------------------------------------------------------------#



#--------------------------- 실수형 ---------------------------#

pi = 3.14
d = 4.15152

print("pi :", pi, ", type(pi) :", type(pi)) # pi : 3.14 , type(pi) : <class 'float'>
print("d :", d, ", type(d) :", type(d)) # d : 4.15152 , type(d) : <class 'float'>

print()

#--------------------------------------------------------------#



#-------------------------- 복소수형 --------------------------#

x = 2j

print("x :", x, ", type(x) :", type(x)) # x : 2j , type(x) : <class 'complex'>

print()

#--------------------------------------------------------------#



'''반복 가능한 자료형
str, list, tuple, dict
'''

#--------------------------- 문자열 ---------------------------#

e = "Hello"
f = "World!"

print("e :", e, ", type(e) :", type(e)) # e : Hello , type(e) : <class 'str'>
print("f :", f, ", type(f) :", type(f)) # f : World! , type(f) : <class 'str'>

print()

#--------------------------------------------------------------#



#--------------------------- 리스트 ---------------------------#

l = [1, "Hello", 3.14, 2j]

print("l :", l, ", type(l) :", type(l)) # l : [1, 'Hello', 3.14, 2j] , type(l) : <class 'list'>

print()

#--------------------------------------------------------------#



#---------------------------- 튜플 ----------------------------#

t = (1, "Hello", 3.14, 2j)

print("t :", t, ", type(t) :", type(t)) # t : (1, 'Hello', 3.14, 2j) , type(t) : <class 'tuple'>

print()

#--------------------------------------------------------------#



#-------------------------- 딕셔너리 --------------------------#

d = {"a": 1, "b": 3.14, "c": "coke"}

print("d :", d, ", type(d) :", type(d)) # d : {'a': 1, 'b': 3.14, 'c': 'coke'} , type(d) : <class 'dict'>

#--------------------------------------------------------------#
